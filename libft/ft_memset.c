#include "libft.h"

void	*ft_memset(void *s, int c, size_t n)
{
	size_t	i;
	t_byte	*buff;

	i = 0;
	buff = (t_byte *)s;
	while (i < n)
		buff[i++] = (t_byte)c;
	return (buff);
}
